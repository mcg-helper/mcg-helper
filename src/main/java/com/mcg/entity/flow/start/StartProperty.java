package com.mcg.entity.flow.start;

import java.io.Serializable;
import java.util.List;

import javax.validation.Valid;
import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@XmlAccessorType(XmlAccessType.NONE)
@XmlRootElement
public class StartProperty implements Serializable {

    private static final long serialVersionUID = -6831068936775340739L;
    @Valid
    @XmlElement
    private List<Var> var;

    public List<Var> getVar() {
        return var;
    }

    public void setVar(List<Var> var) {
        this.var = var;
    }
    
}
